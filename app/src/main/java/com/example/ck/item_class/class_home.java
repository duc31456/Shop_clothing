package com.example.ck.item_class;

public class class_home {
    private int hinh;
    private String mota;

    public class_home(int hinh, String mota) {
        this.hinh = hinh;
        this.mota = mota;
    }

    public int getHinh() {
        return hinh;
    }

    public void setHinh(int hinh) {
        this.hinh = hinh;
    }

    public String getMota() {
        return mota;
    }

    public void setMota(String mota) {
        this.mota = mota;
    }
}
